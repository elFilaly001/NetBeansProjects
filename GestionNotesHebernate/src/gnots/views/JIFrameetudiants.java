    private void btnajoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnajoutActionPerformed
        // TODO add your handling code here:
        String nom=txtnom.getText();
        String prenom=txtprenom.getText();
        Groupe groupe=(Groupe) cbxgroupe.getSelectedItem();
        Etudiant e=new Etudiant(0, groupe, nom, prenom, null);
        Transaction tx=s.beginTransaction();
        s.save(e);
        tx.commit();
        l.add(e);
        JOptionPane.showMessageDialog(rootPane, "Ajout avec succes");
    }//GEN-LAST:event_btnajoutActionPerformed

    private void btnmodifActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodifActionPerformed
        // TODO add your handling code here:
        String nom=txtnom.getText();
        String prenom=txtprenom.getText();
        Groupe groupe=(Groupe) cbxgroupe.getSelectedItem();
        Etudiant e=l.get(pos);
        e=(Etudiant) s.get(Etudiant.class, e.getIdE());
        Transaction tx=s.beginTransaction();
        e.setNom(nom);
        e.setPrenom(prenom);
        e.setGroupe(groupe);
        s.save(e);
        tx.commit();
        JOptionPane.showMessageDialog(rootPane, "modification avec succes");
    }//GEN-LAST:event_btnmodifActionPerformed

    private void btnsuprActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuprActionPerformed
        // TODO add your handling code here:
        Etudiant e=l.get(pos);
        e=(Etudiant) s.get(Etudiant.class, e.getIdE());
        Transaction tx=s.beginTransaction();
        s.delete(e);
        tx.commit();
        pos--;
        afficher();
        JOptionPane.showMessageDialog(rootPane, "supprission avec succes");
    }//GEN-LAST:event_btnsuprActionPerformed
private void afficher() {
        Etudiant e=l.get(pos);
        txtetud.setText(e.getIdE()+"");
        txtnom.setText(e.getNom());
        txtprenom.setText(e.getPrenom());
        cbxgroupe.setSelectedItem(e.getGroupe());
    }

