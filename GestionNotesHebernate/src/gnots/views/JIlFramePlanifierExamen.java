    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        // TODO add your handling code here:
        
        s=HibernateUtil.getSession();
        List<Groupe> lg= s.createQuery("From Groupe").list();
        for( Groupe g:lg)
            cbxgrp.addItem(g);
        s=HibernateUtil.getSession();
        List<Module> lm= s.createQuery("From Module").list();
        for( Module m:lm)
            cbxmod.addItem(m);
        s=HibernateUtil.getSession();
        List<Professeur> lp= s.createQuery("From Professeur").list();
        for( Professeur p:lp)
            cbxprof.addItem(p);
        
    }//GEN-LAST:event_formInternalFrameOpened

    private void btnplanifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnplanifierActionPerformed
        try {
            // TODO add your handling code here:
            Groupe groupe= (Groupe) cbxgrp.getSelectedItem();
            Module module= (Module) cbxmod.getSelectedItem();
            Professeur professeur=(Professeur) cbxprof.getSelectedItem();
            Date date=new SimpleDateFormat("dd/MM/yy").parse(txtdate.getText());
            Examen ex=new Examen(0, groupe, professeur, module, date, null);
            Transaction tx=s.beginTransaction();
            s.save(ex);
            tx.commit();
            JOptionPane.showMessageDialog(rootPane, " examen palnifier avec succes");
        } catch (ParseException ex) {
            Logger.getLogger(JIlFramePlanifierExamen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnplanifierActionPerformed


