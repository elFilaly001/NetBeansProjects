    private void btnrechechActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnrechechActionPerformed
        try {
            // TODO add your handling code here:
            SimpleDateFormat sdf =new SimpleDateFormat("dd/mm/yy");
            Date d1=sdf.parse(txtdat1.getText());
            Date d2=sdf.parse(txtdat2.getText());
            Query q=s.createQuery("Select e from Examen e where  e.groupe.idG=:idg and e.dateexamen between :d1 and :d2 ");
            Groupe g=(Groupe) cbxgrp.getSelectedItem();
            q.setLong("idg",g.getIdG());
            q.setDate("d1", d1);
            q.setDate("d2", d2);
            List<Examen> ex=q.list();
            DefaultTableModel df=(DefaultTableModel) jtres.getModel();
            df.setRowCount(0);
            for(Examen e:ex)
                df.addRow(new Object[]{e.getIdEx(),e.getGroupe().getLibele(),e.getModule().getLibele(),
                    e.getProfesseur().getNomprof(),e.getDateexamen()});
        } catch (ParseException ex) {
            Logger.getLogger(JIFramAfficherPlanning.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnrechechActionPerformed


