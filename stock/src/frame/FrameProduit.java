    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try {
           // TODO add your handling code here:
           Connection con=UtilConn.seConnecter();
           Statement st=con.createStatement();
           ResultSet rs=st.executeQuery("select * from produit");
           while(rs.next()){
            prdt.add(new Produit(rs.getInt(1),rs.getString(2),rs.getInt(3)));
           }
           rs.close();
           con.close();
               
           afficher();
           
           
       } catch (Exception ex) {
           Logger.getLogger(FrameProduit.class.getName()).log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_formWindowOpened

    private void BTNSUPPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNSUPPActionPerformed
        // TODO add your handling code here:
         try {
         Connection  con =UtilConn.seConnecter();
       
           PreparedStatement ps=con.prepareStatement("delete from client where noclt=?");
          Produit c=prdt.get(pos);
           ps.setInt(1,c.getId_produit());
           
          
           ps.executeUpdate();
           prdt.remove(c);
           JOptionPane.showMessageDialog(null,"client supprimer avec succes");
           } catch (Exception ex) {
           Logger.getLogger(FrameProduit.class.getName()).log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_BTNSUPPActionPerformed

    private void BTNAJOUTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNAJOUTActionPerformed
        // TODO add your handling code here:
        try {
           // TODO add your handling code here:
           Connection con=UtilConn.seConnecter();
           PreparedStatement ps=con.prepareStatement("insert into produit values(?,?,?)");
           int id_prdt=Integer.parseInt(TXT_ID_PR.getText());
           String nom=TXT_NOM_PR.getText();
           String qte_prdt=TXT_QTE_PR.getText();
           Produit c=new Produit(id_prdt,nom,Qte_stock);
           ps.setInt(1, id_prdt);
           ps.setString(2, nom);
           ps.setString(3, qte_prdt);
           ps.executeUpdate();
           prdt.add(c);
           JOptionPane.showMessageDialog(null,"client ajouter avec succes");
           
       } catch (Exception ex) {
           Logger.getLogger(FrameProduit.class.getName()).log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_BTNAJOUTActionPerformed

