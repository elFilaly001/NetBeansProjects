    private void btnconnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnconnActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            Connection con = CUtile.getConnected();
            String username = TXTuser.getText();
            String password = JPsswrd.getText();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from compte where usr='"+username+"'and mdp='"+password+"'");
            if (rs.next()){
                dispose();
                FrameP frp = new FrameP();
                frp.show();
            } else {
                JOptionPane.showMessageDialog(this,"username or password are wrong");
                    TXTuser.setText("");
                    JPsswrd.setText("");
            }
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnconnActionPerformed

