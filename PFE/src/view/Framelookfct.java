    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        try {
            DefaultTableModel df1 = (DefaultTableModel) jTable1.getModel();
            DefaultTableModel df2 = (DefaultTableModel) jTable2.getModel();
            int i = jTable1.getSelectedRow();
            Connection con = CUtile.getConnected();
            PreparedStatement ps = con.prepareStatement("select * from facture where id_lnfct=?");
            ps.setInt(1, Integer.parseInt(df1.getValueAt(i, 0).toString()));
            ResultSet rs = ps.executeQuery();
            df2.setRowCount(0);
            while (rs.next()) {
                df2.addRow(new Object[]{ rs.getInt(2), rs.getInt(3), rs.getInt(4), rs.getInt(5)});
            }
        } catch (Exception ex) {
            Logger.getLogger(Framelookfct.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTable1MouseClicked


