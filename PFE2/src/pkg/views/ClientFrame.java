    private void btnRechActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRechActionPerformed
        // TODO add your handling code here:
        Client c = new Client();
        int id = 0;
        String cin = txtCINRech.getText();
        try {
            Connection conn = MyUtil.seConnecter();
            PreparedStatement ps = conn.prepareStatement("select cin_clt,nom_clt,prenom_clt,adresse,tel,permis from client where cin_clt = '" + cin + "'");
            ResultSet rs = ps.executeQuery();
            while(rs.next()) {
                id = 1;
                txtCIN.setText(rs.getString(1));
                txtNom.setText(rs.getString(2));
                txtPrenom.setText(rs.getString(3));
                txtAdresse.setText(rs.getString(4));
                txtTel.setText(rs.getString(5));
                txtPermis.setText(rs.getString(6));
            }
            if(id == 0) {
                JOptionPane.showMessageDialog(null, "CIN Introuvable !");
                txtCIN.setText(null);
                txtNom.setText(null);
                txtPrenom.setText(null);
                txtAdresse.setText(null);
                txtTel.setText(null);
                txtPermis.setText(null);
            }
        } catch (Exception ex) {
            Logger.getLogger(ClientFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRechActionPerformed


