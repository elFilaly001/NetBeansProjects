    private void btnAjoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjoutActionPerformed
        try {
            // TODO add your handling code here:
            Voiture v = new Voiture();
            v.setMatricule(txtMatricule.getText());
            v.setModel(Integer.parseInt(txtModel.getText()));
            v.setMarque(txtMarque.getText());
            v.setCouleur(txtCouleur.getText());
            v.setCarburant((String) cbxCarburant.getSelectedItem());
            if (rdbtnEnServ.isSelected()) {
                v.setEtat("En service");
            } else {
                v.setEtat("Hors service");
            }
            v.setPrix(Float.parseFloat(txtPrix.getText()));

            Connection conn = MyUtil.seConnecter();
            PreparedStatement ps = conn.prepareStatement("insert into voiture values(?,?,?,?,?,?,?)");
            ps.setString(1, v.getMatricule());
            ps.setString(2, v.getModel() + "");
            ps.setString(3, v.getMarque());
            ps.setString(4, v.getCouleur());
            ps.setString(5, v.getCarburant());
            ps.setString(6, v.getEtat());
            ps.setString(7, v.getPrix() + "");
            ps.executeUpdate();
            ps.close();
            conn.close();
            lv.add(v);
            df.addRow(new Object[]{v.getMatricule(), v.getModel(), v.getMarque(), v.getCouleur(), v.getCarburant(), v.getEtat(), v.getPrix()});
            JOptionPane.showMessageDialog(null, "Voiture ajouté avec succes.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "erreur" + ex.getMessage());
        }

    }//GEN-LAST:event_btnAjoutActionPerformed

    private void btnSuppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuppActionPerformed
        try {
            // TODO add your handling code here:
            Voiture v = lv.get(pos);
            Connection conn = MyUtil.seConnecter();
            PreparedStatement ps = conn.prepareStatement("delete from voiture where matricule=?");
            ps.setString(1, v.getMatricule());
            ps.executeUpdate();

            try {
                lv.remove(v);
                df.removeRow(pos);
                pos--;
                afficher();
            } catch (IndexOutOfBoundsException ex) {
                pos++;
                afficher();
            }

            if (lv.isEmpty()) {
                txtMatricule.setText("");
                txtMarque.setText("");
                txtCouleur.setText("");
                txtPrix.setText("");
                txtModel.setText("");
                cbxCarburant.setSelectedItem("");
                rdbtnEnServ.setSelected(false);
                rdbtnHorsServ.setSelected(false);
            }
            JOptionPane.showMessageDialog(null, "Voiture supprimé avec succes");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "" + ex.getMessage());
        }
    }//GEN-LAST:event_btnSuppActionPerformed

