    private void btnMatRechActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMatRechActionPerformed
        // TODO add your handling code here:
        Voiture v = new Voiture();
        int id = 0;
        String mat = txtMatRech.getText();
        try {
            Connection conn = MyUtil.seConnecter();
            PreparedStatement ps = conn.prepareStatement("select matricule,model,marque,couleur,carburant,etat,prix from voiture where matricule = '" + mat + "'");
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                id = 1;
                txtMatricule.setText(rs.getString(1));
                txtModel.setText(rs.getInt(2) + "");
                txtMarque.setText(rs.getString(3));
                txtCouleur.setText(rs.getString(4));
                cbxCarburant.setSelectedItem(rs.getString(5));
                if (rs.getString(6).equals("En service")) {
                    rdbtnEnServ.setSelected(true);
                } else {
                    rdbtnHorsServ.setSelected(true);
                }
                txtPrix.setText(rs.getFloat(7) + "");
            }
            if(id == 0) {
                JOptionPane.showMessageDialog(null, "Matricule Introuvable !");
                txtMatricule.setText(null);
                txtModel.setText(null);
                txtMarque.setText(null);
                txtCouleur.setText(null);
                cbxCarburant.setSelectedItem(null);
                rdbtnEnServ.setSelected(false);
                rdbtnHorsServ.setSelected(false);
                txtPrix.setText(null);
            }
        } catch (Exception ex) {
            Logger.getLogger(CarFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnMatRechActionPerformed


