    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            stock c = new stock();
            c.setId_prd(Integer.parseInt(jTextField4.getText()));
            c.setNom_prd(jTextField1.getText());
            c.setQte_prd(Integer.parseInt(jTextField2.getText()));
            c.setPrix(Integer.parseInt(jTextField3.getText()));
            Connection con = CUtile.getConnected();
            PreparedStatement ps = con.prepareStatement("insert into stock values(seq_id_prd.nextval,?,?,?)");
            //ps.setInt(1,jTextField4.getText()+"");
            ps.setString(1, jTextField1.getText());
            ps.setInt(2,Integer.parseInt(jTextField2.getText()));
            ps.setInt(3,Integer.parseInt(jTextField3.getText()));
            ps.executeUpdate();
            
            st.add(c);
            pos = st.size() - 1;
            JOptionPane.showMessageDialog(null, "ajouter avec succes");
        } catch (Exception ex) {
            Logger.getLogger(FrameAj.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

