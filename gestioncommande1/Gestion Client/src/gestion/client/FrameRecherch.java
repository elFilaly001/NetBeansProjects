    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here
        String mot=txtmot.getText();
        String req="select * from Client";
        if(rdtnomp.isSelected()){
            req+=" where lower(nom ||' '||prenom) like'%"+mot+"%'";
            
        }
         if(rdtadresse.isSelected()){
            req+=" where lower(adresse) like'%"+mot+"%'";
         }
         try{
             Connection con=MyUtil.seConnecter();
             PreparedStatement ps=con.prepareStatement(req);
             ResultSet rs=ps.executeQuery();
              DefaultTableModel dfs=(DefaultTableModel) jTable2.getModel();
              dfs.setRowCount(0);
             while(rs.next()){
                 dfs.addRow(new Object[]{rs.getInt(1),rs.getString(2),rs.getString(3),rs.getString(4)});
             }
             rs.close();
             con.close();
         } catch (Exception ex) {
            Logger.getLogger(FrameRecherch.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

