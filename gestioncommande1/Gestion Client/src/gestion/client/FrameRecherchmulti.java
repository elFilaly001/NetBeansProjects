    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here
        
        
        String req="select  noclt,nom,prenom,adresse,nocmd,datecmd,sum(prix*qte)"+"from client join commande using(noclt) join lignecommande using (nocmd) join article using(noart)";
        if(cbknom.isSelected()){
            req+=" where lower(nom ||' '||prenom) like'%"+txtnom.getText()+"%'";
            
        }
         if(cbkadresse.isSelected()){
             if(cbknom.isSelected())
                 req+="and";
             else
                req+="where "; 
             
            req+="  lower(adresse) like'%"+txtadresse.getText()+"%'";
         }
         if(cbkdatecmd.isSelected()){
             if(cbknom.isSelected() ||cbkadresse.isSelected())
                req+= "and";
             else
                 req+="where "; 
             req+="datecmd between '"+txtdate1.getText()+"' and '"+txtdate2.getText()+"'";
                 
             
             
         }
         req+="group by noclt,nom,prenom,adresse,nocmd,datecmd";
         try{
             Connection con=MyUtil.seConnecter();
             PreparedStatement ps=con.prepareStatement(req);
              ResultSet rs=ps.executeQuery();
              DefaultTableModel dfs=(DefaultTableModel) jTable2.getModel();
              dfs.setRowCount(0);
             while(rs.next()){
                 dfs.addRow(new Object[]{rs.getInt(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getInt(5),rs.getDate(6),rs.getFloat(7)});
             }
             rs.close();
             con.close();
         } catch (Exception ex) {
            Logger.getLogger(FrameRecherchmulti.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

