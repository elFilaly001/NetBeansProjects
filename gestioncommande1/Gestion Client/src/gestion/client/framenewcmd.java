    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:C
       Client c=(Client) cbxclt.getSelectedItem();
       String  datecmd=(txtdatecmd.getText());
     //  int qte=Integer.parseInt(txtqte.getText()+"");
       DefaultTableModel df=(DefaultTableModel) listart.getModel();
       Connection  con=null;
     try {
         con=MyUtil.seConnecter();
         con.setAutoCommit(false);
         PreparedStatement ps=con.prepareStatement("insert into commande values(seq_cmd,?,?)",new int[]{1});
         ps.setString(1,datecmd);
         ps.setInt(2,c.getNoclt());
         
         ps.executeUpdate();
         ResultSet rs=ps.getGeneratedKeys();
         if(rs.next()){
             int nocmd=rs.getInt(1);
             PreparedStatement psl=con.prepareStatement("insert into lignecommande values(seq_lc,?,?,?)",new int[]{1});
             psl.setInt(1,nocmd);
             
             
             
             for(int i=0;i<listart.getRowCount();i++ ){
                 psl.setInt(2,Integer.parseInt(df.getValueAt(i,0).toString()));
                 psl.setInt(3,Integer.parseInt(df.getValueAt(i,3).toString()));
                 psl.executeUpdate();
                 
                 
             }
             
             con.commit();
             JOptionPane.showMessageDialog(rootPane,"souvgarde avc succes");
             
         }
         
     } catch (Exception ex) {
           try {
               con.rollback();
           } catch (SQLException ex1) {
               Logger.getLogger(framenewcmd.class.getName()).log(Level.SEVERE, null, ex1);
           }
         Logger.getLogger(framenewcmd.class.getName()).log(Level.SEVERE, null, ex);
     }
      
       
       
    }//GEN-LAST:event_jButton2ActionPerformed

