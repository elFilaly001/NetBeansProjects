    private void btnajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnajouterActionPerformed
    try {
        // TODO add your handling code here:
        rs.moveToInsertRow();//creer nouvel ligne
        rs.updateInt(1,Integer.parseInt(txtnoart.getText()+""));
        rs.updateString(2, txtlibele.getText());
        rs.updateFloat(3,Float.parseFloat(txtprix.getText()+""));
         rs.updateInt(4,Integer.parseInt(txtqte.getText()+""));
         rs.insertRow();
         rs.moveToCurrentRow();
         JOptionPane.showMessageDialog(rootPane, "article ajouter avec succes");
        
    } catch (SQLException ex) {
        Logger.getLogger(FrameArticle.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_btnajouterActionPerformed

