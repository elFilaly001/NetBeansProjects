    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Connection con = UtilConnexion.seConnecter();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * from association WHERE ID_ASS LIKE '%" + txt.getText() + "%'");
            if (rs.next()) {
                list.add(new association(rs.getInt(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getInt(5), rs.getString(6)));
            }

            rs.close();
            con.close();
           afficher();
            JOptionPane.showMessageDialog(null, "Association trouve avec succés!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Association non trouve!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

