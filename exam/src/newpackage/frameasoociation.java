    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
        try {
            Connection con=UtilConnexion.seConnecter();
            PreparedStatement ps=con.prepareStatement("insert into association values(?,?,?,?,?,?)");
            int id_ass=Integer.parseInt(txt.getText());
            String nom=txtn.getText();
            String raisonsocial=txtr.getText();
            String adresse=txta.getText();
            int tele=Integer.parseInt(txtt.getText());
            String id_ville=txtv.getSelectedItem().toString();
            
            association a=new association(id_ass, nom,raisonsocial,adresse,tele,id_ville);
            ps.setInt(1, id_ass);
            ps.setString(2, nom);
            ps.setString(3, raisonsocial);
            ps.setString(4, adresse);
            ps.setInt(5, tele);
            ps.setString(6,id_ville);
            ps.executeUpdate();
            
            list.add(a);
            pos=list.size()-1;
            
            JOptionPane.showMessageDialog(null, "Ajout avec succes.");
                   
        } catch (Exception ex) {
            Logger.getLogger(frameasoociation.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

