    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        lbno.setText(clts.getNoclt()+"");
        lbnom.setText(clts.getNom());
        lbprenom.setText(clts.getPrenom());
        lbadresse.setText(clts.getAdresse());
        
    try {
        Connection con=MyUtil.seConnecter();
        PreparedStatement ps=con.prepareStatement("select cmd.nocmd,datecmd,sum(prix*qte) as total from Commande cmd join ligneCommande lc on(lc.nocmd=cmd.nocmd) join article a on(lc.noart=a.noart)  where cmd.noclt=? group by cmd.nocmd,datecmd" );
        ps.setInt(1, clts.getNoclt());
        ResultSet rs=ps.executeQuery();
       DefaultTableModel df=(DefaultTableModel) listcmd.getModel();
       df.setRowCount(0);
       
       while(rs.next()){
           df.addRow(new Object[]{rs.getInt(1),rs.getDate(2),rs.getFloat(3)});
           
           
       }
       rs.close();
       con.close();
        
    } catch (Exception ex) {
        Logger.getLogger(frameCmdClient.class.getName()).log(Level.SEVERE, null, ex);
    }
        
        
    }//GEN-LAST:event_formWindowOpened

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         int row=listcmd.getSelectedRow();
         if(row<0){
             JOptionPane.showMessageDialog(null, "selectionner un ligne");
             
         }else{
              DefaultTableModel df=(DefaultTableModel) listcmd.getModel();
              int no=Integer.parseInt(df.getValueAt(row, 0).toString());
              java.sql.Date datecmd=java.sql.Date.valueOf(df.getValueAt(row, 1).toString());
              new FrameDetailCmd(clts,no,datecmd);
              
              
                      
         }
         
         
         
    }//GEN-LAST:event_jButton1ActionPerformed

